package com.example.Model;

public class PriceLevel {

    private Integer id;
    private String name;
    private Integer sequence;
    private Integer salesSequence;
    private Integer retailSequence;
    private Integer pOSSequence;
    private String color;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getSequence() {
        return sequence;
    }

    public void setSequence(Integer sequence) {
        this.sequence = sequence;
    }

    public Integer getSalesSequence() {
        return salesSequence;
    }

    public void setSalesSequence(Integer salesSequence) {
        this.salesSequence = salesSequence;
    }

    public Integer getRetailSequence() {
        return retailSequence;
    }

    public void setRetailSequence(Integer retailSequence) {
        this.retailSequence = retailSequence;
    }

    public Integer getpOSSequence() {
        return pOSSequence;
    }

    public void setpOSSequence(Integer pOSSequence) {
        this.pOSSequence = pOSSequence;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }


}
